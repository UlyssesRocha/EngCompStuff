    private void serverConnection() throws RemoteException, NotBoundException{
        if(!auth){
         r = LocateRegistry.getRegistry(serverTxt.getText(), 1099);
         service = (MyInterface)r.lookup("service");
         auth = true;
        }
    }
    
    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        try {
            // TODO add your handling code here:
            serverConnection();
            String token = service.login(loginTxt.getText(), passTxt.getText());
            if (token != null){
               System.out.println("Login realizado com sucesso Token "+token);
               Client master = new Client(serverTxt.getText(),loginTxt.getText(), token);
               master.start();
               this.setVisible(false);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginBtnActionPerformed

